# Set - It is a collection of non-repetitive elements

a = {1, 2, 3, 4, 5, 2, 4}

print(a)         # In output the repetitive number will not come. {1, 2, 3, 4, 5} - output


# --------------------------------------------------------------------------------------------------
a = {}  # This is an empty dictionary but not an empty set.

print(type(a))    # Class is dictionary


# --------------------------------------------------------------------------------------------------
b = () # This is an empty tuple but not an empty set.

print(type(b))  # Class is tuple

# --------------------------------------------------------------------------------------------------
c = set() # This is an empty set.

print(type(c))   # Class is set

