# To create a lists using square bracket []

# Creating a list

a = [1, 2, 5, 12, 5, 7, 9]

print(a)


# Access a list using index a[0], a[1], a[2]

print(a[2])

# To change the value of one from the list.

a[3] = 45

# Similarly, to change the value of 1th index.

a[0]  = 10

print(a)

# We can also create a list with different data types

a = [25, 'Anand', 25.5, True]

print(a)