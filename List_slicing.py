# List Slicing

Friends = ["Harry", "Tom", "Sam", "Anand", 45, 'Rohit', 'Rohan', 'Omkar']

print(Friends)

print(Friends[0 : 2])     # [Index : Slicing] Value till that index number or name.

# Similarly, 

print(Friends[2 : 6])
 
 
# Similarly with neagtive integer.

print(Friends[-4 : ])