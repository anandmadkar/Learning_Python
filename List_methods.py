# ------------------------------------------ Methods in lists -----------------------------------------

l1 = [1, 2, 3, 5, 10, 3, 15, 14]

#---------------- Sort Method ---------------

l1.sort()

#---------------  Reverse Method --------------

l1.reverse()

#--------------- Append Method  ----------------

l1.append(25)    # it will add 25 at the end of the list

# -------------- Insert Method -----------------

l1.insert(2, 4)   # this is adding or inserting 4 number at the place of 2nd index number.

# -------------- Pop Method --------------------

l1.pop(5)   # this will remove 5th index number from the list.


#  ------------------- To Print the list ----------------------

print(l1)